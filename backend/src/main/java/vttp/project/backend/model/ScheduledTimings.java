package vttp.project.backend.model;

public class ScheduledTimings {

    private String beforeBreakfast;
    private String afterBreakfast;
    private String beforeLunch;
    private String afterLunch;
    private String beforeDinner;
    private String afterDinner;

    public ScheduledTimings() {}

    public String getBeforeBreakfast() {return beforeBreakfast;}
    public void setBeforeBreakfast(String beforeBreakfast) {this.beforeBreakfast = beforeBreakfast;}

    public String getAfterBreakfast() {return afterBreakfast;}
    public void setAfterBreakfast(String afterBreakfast) {this.afterBreakfast = afterBreakfast;}

    public String getBeforeLunch() {return beforeLunch;}
    public void setBeforeLunch(String beforeLunch) {this.beforeLunch = beforeLunch;}

    public String getAfterLunch() {return afterLunch;}
    public void setAfterLunch(String afterLunch) {this.afterLunch = afterLunch;}

    public String getBeforeDinner() {return beforeDinner;}
    public void setBeforeDinner(String beforeDinner) {this.beforeDinner = beforeDinner;}

    public String getAfterDinner() {return afterDinner;}
    public void setAfterDinner(String afterDinner) {this.afterDinner = afterDinner;}

    
    
}
